// Active language
let currentLang = localStorage.getItem('vcms_lang') || 'en';

function t(key) {
  return TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS.en[key] || key;
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('vcms_lang', lang);
  // Apply RTL if needed
  document.documentElement.dir = TRANSLATIONS[lang].dir || 'ltr';
  document.documentElement.lang = lang;
  // Re-translate all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
}

function getLanguageOptions() {
  return Object.entries(TRANSLATIONS).map(([code, data]) =>
    `<option value="${code}" ${code === currentLang ? 'selected' : ''}>${data.flag} ${data.name}</option>`
  ).join('');
}

// Apply on load
document.addEventListener('DOMContentLoaded', () => setLanguage(currentLang));
